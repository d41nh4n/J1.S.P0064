public class InputString {
    private String input;

    public InputString(String input) {
        this.input = input;
    }

    public String getInput() {
        return input;
    }

    public void setInput(String input) {
        this.input = input;
    }

    @Override
    public String toString() {
        return "InputString{" + "input=" + input + '}';
    }
    
    
}
